def xor_with_41(data):
    result = []
    for byte in data:
        xor_byte = byte ^ 0x41
        result.append(chr(xor_byte))
    return ''.join(result)

def main():
    data = [
        0x32, 0x36, 0x20, 0x2C, 0x31, 0x02, 0x15, 0x07, 
        0x3A, 0x19, 0x71, 0x13, 0x1E, 0x28, 0x2F, 0x37, 
        0x71, 0x2D, 0x34, 0x35, 0x28, 0x71, 0x2F, 0x1E, 
        0x28, 0x74, 0x1E, 0x22, 0x71, 0x71, 0x2d, 0x3c
    ]
    flag = xor_with_41(data)

    print("Flag:", flag)

if __name__ == "__main__":
    main()
